        inline uint32_t GetHash(uintptr_t ped)
        {
            uintptr_t weaponManager = mem.Read<uintptr_t>(ped + game::offset::weaponManager);
            if (!weaponManager)
                return 0;

            uintptr_t currentWeapon = mem.Read<uintptr_t>(weaponManager + 0x20);
            if (!currentWeapon)
                return 0;

            uint32_t weaponHash = mem.Read<uint32_t>(currentWeapon + 0x10);

            return weaponHash;
        }

        inline const char* GetName(uint32_t weaponHash)
        {
            auto it = weaponNames.find(weaponHash);
            return (it != weaponNames.end()) ? it->second : "Custom";
        }
